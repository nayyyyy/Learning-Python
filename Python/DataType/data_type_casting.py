def casting_integer():
    # This is how to convert data types to INT
    print(int(1))
    print(int('3'))
    print(int(3.13))


def casting_float():
    # This is how to convert data types to FLOAT
    print(float(3))
    print(float('3.14'))
    print(float('23/4'))


def casting_string():
    # This is how to convert data types to String
    print(str('3.14'))
    print(str(22/8))
    print(str(-1112))
